#Python - Change Dictionary Items
#Change Dictionary Items
#Once a dictionary is created, you can change its values.
#Example
#Change the "year" to 2018:
thisdict = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964
}
thisdict["year"] = 2018
print(thisdict) #Output: {'brand': 'Ford', 'model': 'Mustang', 'year': 2018}
