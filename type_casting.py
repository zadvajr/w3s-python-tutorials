#type casting in python

#convert int to float
i = 10
print(float(i)) #prints 10.0

#converting float to int
j = 10.7
print(int(j)) # prints 10

#convert string to int
pin = "1234"
print(int(pin)) # prints 1234

#converting numbers to string
height = 1.75
print(str(height)) # prints 1.75 as a string

#rounding numbers
num = 23.978643
print(round(num, 2)) # prints 23.98