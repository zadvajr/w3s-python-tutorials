# tuple methods

#count() returns the number of times a certain value appears in a tuple

fruits = ("Apple", "Banana", "Orange", "Apple", "Citrus", "Mango", "Apple")

print(fruits.count("Apple")) # returns 3, the number of times apple appear

#end of count

#index() metho

print(fruits.index("Banana")) # returns 1 the first index where Banana occurs